def wheel(pos):
    pos = pos % 256
    if pos < 85:
        return (255 - pos * 3, pos * 3, 0)
    elif pos < 170:
        pos -= 85
        return (0, 255 - pos * 3, pos * 3)
    else:
        pos -= 170
        return (pos * 3, 0, 255 - pos * 3)

def draw_emoji(pixels, color, brightness=1.0):
    c = tuple(int(v * brightness) for v in color)
    for y in range(8):
        for x in range(8):
            np[xy(x, y)] = c if pixels[y][x] else (0, 0, 0)
    np.write()

