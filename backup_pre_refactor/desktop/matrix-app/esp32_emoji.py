LED_PIN = Pin(2)
NUM_LEDS = 64
MATRIX = 8
np = neopixel.NeoPixel(LED_PIN, NUM_LEDS)

def xy(x, y):
    return y * MATRIX + (MATRIX - 1 - x) if y % 2 else y * MATRIX + x

def clear():
    for i in range(NUM_LEDS):
        np[i] = (0, 0, 0)
    np.write()

def dim(c, f=0.3):
    return tuple(int(v * f) for v in c)

