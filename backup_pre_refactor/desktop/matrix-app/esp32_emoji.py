def main():
    names = list(EMOJIS.keys())
    clear()

    print("=" * 40)
    print("Matrix 8x8 - 20 Emoji Animations")
    print("=" * 40)

    # Intro scroll
    show_text_scroll("EMOJIS!", (0, 255, 200), 0.06)
    time.sleep(0.5)

    i = 0
    while True:
        name = names[i % len(names)]
        emoji = EMOJIS[name]
        print(f"[{i+1}] {name}")

        # Flash in effect
        clear()
        for step in range(10):
            b = step / 10.0
            for y in range(8):
                for x in range(8):
                    if emoji["pixels"][y][x]:
                        np[xy(x, y)] = tuple(int(v * b) for v in emoji["color"])
            np.write()
            time.sleep(0.03)

        # Pulse effect
        for _ in range(15):
            for p in [0.7, 1.0, 0.85, 1.0]:
                for y in range(8):
                    for x in range(8):
                        if emoji["pixels"][y][x]:
                            np[xy(x, y)] = tuple(int(v * p) for v in emoji["color"])
                        else:
                            np[xy(x, y)] = (0, 0, 0)
                np.write()
                time.sleep(0.12)

        # Fade out
        for step in range(8, -1, -1):
            b = step / 8.0
            for y in range(8):
                for x in range(8):
                    if emoji["pixels"][y][x]:
                        np[xy(x, y)] = tuple(int(v * b) for v in emoji["color"])
            np.write()
            time.sleep(0.04)

        i += 1
        time.sleep(0.2)


if __name__ == "__main__":
    main()
