srv = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
srv.setsockopt(socket.SOL_SOCKET, socket.SO_REUSEADDR, 1)
srv.bind(('0.0.0.0', 80))
srv.listen(1)
srv.settimeout(0.05)

names = list(EMOJIS.keys())
idx = 0
print('HTTP ready')

while True:
    try:
        c, a = srv.accept()
        c.settimeout(0.3)
        try:
            d = c.recv(512)
            if d:
                t = d.decode('utf-8', 'ignore')
                p = t.find('\r\n\r\n')
                if p >= 0:
                    bd = t[p+4:].strip()
                    try:
                        cmd = json.loads(bd)
                        tp = cmd.get('t','')
                        if tp == 'emoji':
                            sel = cmd.get('n','')
                            auto = False
                        elif tp == 'bri':
                            bri = max(1, min(100, int(cmd.get('v',80))))
                        elif tp == 'auto':
                            auto = not auto
                            if auto: sel = None
                    except: pass
                c.send(b'HTTP/1.1 200 OK\r\nAccess-Control-Allow-Origin: *\r\n\r\nok')
        except: pass
        try: c.close()
        except: pass
    except OSError: pass

    tick += 1
    if auto and tick % 50 == 0:
        idx = (idx + 1) % len(names)
        sel = names[idx]

    if sel and sel in EMOJIS:
        px, clr = EMOJIS[sel]
        draw(px, clr)
    else:
        b = int(8 + 6 * __import__('math').sin(tick * 0.15))
        for i in range(NUM_LEDS): np[i] = (0, 0, b)
        np.write()

    time.sleep(0.06)
